    public void tampilRealisasi() {
        Realisasi realisasi2 = new Realisasi();
        DefaultTableModel model = (DefaultTableModel) jTbl_Realisasi.getModel();
        Object[] atributreal = new Object[7];
        try {
            ArrayList tampil = realisasi2.viewSaja(); //harus dikasih try catch
            Iterator listrealisasi = tampil.iterator();

            int i = 0;
            while (i < tampil.size()) {
                Realisasi eachreal;
                eachreal = (Realisasi) listrealisasi.next();
                atributreal[0] = eachreal.getIdLaporan();
                atributreal[1] = eachreal.getIdDariUser();
                atributreal[2] = eachreal.getIdDariProker();
                atributreal[3] = eachreal.getTglRealisasi();
                atributreal[4] = eachreal.getAnggaran();
                atributreal[5] = eachreal.getDeskripsi();
                atributreal[6] = eachreal.getStatus();
                //System.out.println(eachKepsek.getIdKepsek()+"-"+eachKepsek.getNamaKepsek());
                model.addRow(atributreal);
                i++;
            }
            jTbl_Realisasi.setModel(model);
        } catch (Exception ex) {
            System.out.println(ex.getMessage());

        }

    }

