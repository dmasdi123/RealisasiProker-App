    public void tampildosen() {
        try {
            String query = "select * from dosen where idDosen='" + lb_iduser.getText() + "'";
            PreparedStatement statement = DatabaseMySQL.getConnection().prepareStatement(query);
            ResultSet rs = statement.executeQuery();
            while (rs.next()) {
                id_dosen.setText(String.valueOf(rs.getInt("idDosen")));
                nama_dosen.setText(rs.getString("nama_dosen"));
                npp.setText(String.valueOf(rs.getInt("NPP")));
                ttl.setText(String.valueOf(rs.getDate("ttl_dosen")));
                agama.setText(rs.getString("agama_dosen"));
                jekel.setText(rs.getString("jenis_kelamin"));
            }

            statement.close();
            rs.close();
        } catch (Exception ex) {
            System.out.println(ex.getMessage());
        }

    }

