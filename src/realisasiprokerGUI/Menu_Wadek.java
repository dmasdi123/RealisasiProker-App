        String query = "update dosen set NPP=?, nama_dosen=? , ttl_dosen=?, agama_dosen=?, jenis_kelamin=? where idDosen=?";
        PreparedStatement statement = DatabaseMySQL.getConnection().prepareStatement(query);

        //SimpleDateFormat date = new SimpleDateFormat("yyyy-MM-dd");
        statement.setString(1, npp.getText());
        statement.setString(2, nama_dosen.getText());
        statement.setString(3, ttl.getText());
